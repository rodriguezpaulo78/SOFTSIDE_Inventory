    /**
     * Modificar un registro de proveedor en la BD previa validación
     */
    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnAceptarActionPerformed
    {//GEN-HEADEREND:event_btnAceptarActionPerformed
       
        if (txtProvRazSoc.getText().equals("")||txtProvNomRep.getText().equals("")||txtProvRuc.getText().equals("")
                ||txtProvRub.toString().equals("")||txtProvTel.getText().equals("")){
            JOptionPane.showMessageDialog(null, "No pueden estar campos vacios");
        }
        else {
            interfaz.aceptar(this.txtProvCod, this.txtProvRazSoc, this.txtProvNomRep, this.txtProvRuc, this.txtProvRub, this.txtProvTel);
        }
        
        
    }//GEN-LAST:event_btnAceptarActionPerformed

